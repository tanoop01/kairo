// Helper function to get nested translation
export const getNestedTranslation = (translations, key, language = 'en') => {
  const keys = key.split('.');
  let result = translations[language];
  
  for (const k of keys) {
    if (result && result[k]) {
      result = result[k];
    } else {
      // Fallback to English if key not found
      result = translations.en;
      for (const k of keys) {
        if (result && result[k]) {
          result = result[k];
        } else {
          return key; // Return the key if not found
        }
      }
    }
  }
  
  return result || key;
};